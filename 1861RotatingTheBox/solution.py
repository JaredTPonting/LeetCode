def rotateTheBox(box: list[list[str]]) -> list[list[str]]:
    def organise_cols(col: list[str]) -> list[str]:
        blocks = [i for i, v in enumerate(col) if v == '*']
        hash = [i for i, v in enumerate(col) if v == '#']
        empt = [i for i, v in enumerate(col) if v == '.']
        organised = ['.' if i != '*' else i for i in col]
        if (not hash and not blocks) or not hash:
            return organised
        if not blocks:
            organised[-len(hash):] = ['#' for i in hash]
            return organised
        for i in blocks:
            bef = [loc for loc in hash if loc < i]
            hash = [i for i in hash if i not in bef]
            organised[i - len(bef):i] = ['#' for i in bef]
        return organised

    to_ret = [organise_cols(col) for col in box]
    return [list(e) for e in zip(*to_ret[::-1])]


